func lengthOfLongestSubstring(s string) int {
	last,max,count:=1,0,0
	m:=[128]int{}
	for index,value := range s{
		if m[value] < last{
			count ++
		} else {
			last = m[value]
			if count > max{
				max = count
			}
			count = index - last +1
		}
		m[value] = index +1
	}
	if count > max {
		max = count
	}
	return max
}

